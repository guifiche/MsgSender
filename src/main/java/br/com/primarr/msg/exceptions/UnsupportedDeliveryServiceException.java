package br.com.primarr.msg.exceptions;

public class UnsupportedDeliveryServiceException extends MessagingException {

	public UnsupportedDeliveryServiceException() {
		
	}

	public UnsupportedDeliveryServiceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public UnsupportedDeliveryServiceException(String arg0) {
		super(arg0);
		
	}

	public UnsupportedDeliveryServiceException(Throwable arg0) {
		super(arg0);
		
	}
	
	
}
